public class Rat {

    private int hp = 10, max_damage = 7, min_damage = 5;

    public void setHp(int hp)
    {
        this.hp = hp;
    }

    public int getHp()
    {
        return hp;
    }

    public int getMax_damage()
    {
        return max_damage;
    }

    public int getMin_damage()
    {
        return min_damage;
    }
}
