public class Zombie {

    private int hp = 30, max_damage = 4, min_damage = 2;

    public void setHp(int hp)
    {
        this.hp = hp;
    }

    public int getHp()
    {
        return hp;
    }

    public int getMax_damage()
    {
        return max_damage;
    }

    public int getMin_damage()
    {
        return min_damage;
    }
}
