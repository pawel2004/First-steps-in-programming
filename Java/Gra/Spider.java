public class Spider {

    private int hp = 20, max_damage = 5, min_damage = 1;

    public void setHp(int hp)
    {
        this.hp = hp;
    }

    public int getHp()
    {
        return hp;
    }

    public int getMax_damage()
    {
        return max_damage;
    }

    public int getMin_damage()
    {
        return min_damage;
    }

}
