public class Ticket {

    private String code;
    private int duration;

    public void setCode(String code) {
        this.code = code;
    }

    public void setDuration(int d) {
        this.duration = d;
    }

    public String getCode()
    {
        return code;
    }

    public int getDuration()
    {
        return duration;
    }
}
